// Template literals
// Support multiline string(use 1)
let name=`Aditya
           Raj
           Rathore`
console.log(name)

// Support html tags (use 2)
let name1='<h1>Aditya Raj Rathore</h1>'
document.write(name1)

// Support double and single quotes(use 3)
let name2='Aditya "Raj" Rathore'
console.log(name2)

// (use 4)
let name3="Aditya"
let age=20
console.log(`My name is ${name3} and my age ${age} is years old`);
